    // ++ increment  = +1 
    // -- decrement = -1 

    let num1  = 32
    let num2 = 23
    console.log(num1%num2)

    console.log(num1**2)
    
    // prefix 
    // ++x 

    let x = 20
    let inc = ++x 
    console.log("pre",inc)
    console.log("inc",x)
    document.write(x,"<br>")
    document.write(inc)


    //  postfix 
    // x++ 

    let x1 = 20
    let inc1 = x++
    console.log("post",inc1)
    console.log("inc1",x1)




    



